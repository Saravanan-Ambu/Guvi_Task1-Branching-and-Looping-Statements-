package branching_looping_statements;

public class Program_1 {
    public static void main(String[] args) {
            for (int i = 10; i <=50; i++) {
                System.out.println("The value of:"+ i);
            }

            int i = 10;
            while(i <=50){
                System.out.println("The value of:"+ i);
                i++;
            }
    }
}
